index='141700'
x=0
y=0
for number in index:
    list_coords = numbers_dict[number]
    start = list_coords[0]
    t.penup()
    vert,hor=start
    x += hor
    y += vert
    t.goto(x,y)
    t.pendown()
    for coords in list_coords[1:-1]:
        s,a = coords
        t.forward(s)
        t.left(a)
    t.penup()
    finish = list_coords[-1]
    vert,hor = finish
    x += hor
    y += vert
    t.goto(x,y)
